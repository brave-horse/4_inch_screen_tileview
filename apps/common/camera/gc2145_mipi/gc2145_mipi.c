void GC2145_MIPI_reset(u8 isp_dev)
{
    u8 res_io;
    u8 powd_io;
    u8 id = 0;
    puts("GC2145 reset\n");

    res_io = gc2145_reset_io[0];
    powd_io = gc2145_power_io[0];

    if (powd_io != (u8) - 1) {
        GC2145_MIPI_powerio_ctl(powd_io, 1);
        delay(40000);
        GC2145_MIPI_powerio_ctl(powd_io, 0);
    }
    if (res_io != (u8) - 1) {
        gpio_direction_output(res_io, 0);
        delay(40000);
        gpio_direction_output(res_io, 1);
        delay(40000);
    }

}

s32 GC2145_MIPI_check(u8 isp_dev, u32 _reset_gpio, u32 _power_gpio, char *iic_name)
{
    if (!iic) {
        iic = dev_open(iic_name, 0);
        if (!iic) {
            return -1;
        }
    } else {
        if (cur_sensor_type != isp_dev) {
            return -1;
        }
    }
    gc2145_reset_io[isp_dev] = (u8)_reset_gpio;
    gc2145_power_io[isp_dev] = (u8)_power_gpio;

    GC2145_MIPI_reset(isp_dev);

    if (0 != GC2145_MIPI_ID_check()) {
        dev_close(iic);
        iic = NULL;
        return -1;
    }

    cur_sensor_type = isp_dev;

    return 0;
}

