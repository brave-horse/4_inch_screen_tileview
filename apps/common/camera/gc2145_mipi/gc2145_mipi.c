s32 GC2145_MIPI_ID_check(void)
{
    u8 pid = 0x00;
    u8 ver = 0x00;

    for (int i = 0; i < 3; i++) {
        rdGC2145Reg(0xf0, &pid);
        rdGC2145Reg(0xf1, &ver);
        os_time_dly(10);
    }

    if (pid != 0x21 || ver != 0x45) {
        puts("\n----not GC2145_MIPI-----\n");
        return -1;
    }
    puts("\n----hello GC2145_MIPI-----\n");
    return 0;

}

